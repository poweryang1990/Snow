namespace UOKOFramework
{
    /// <summary>
    /// 时间的格式化格式
    /// </summary>
    // ReSharper disable InconsistentNaming
    public static class DateTimeFormats
    {
        /// <summary>
        /// yyyy-MM
        /// </summary>
        public const string yyyyMM = "yyyy-MM";

        /// <summary>
        /// yyyy-MM-dd
        /// </summary>
        public const string yyyyMMdd = "yyyy-MM-dd";

        /// <summary>
        /// yyyy-MM-dd HH:mm
        /// </summary>
        public const string yyyyMMddHHmm = "yyyy-MM-dd HH:mm";

        /// <summary>
        /// yyyy-MM-dd HH:mm:ss
        /// </summary>
        public const string yyyyMMddHHmmss = "yyyy-MM-dd HH:mm:ss";

        /// <summary>
        /// yyyy-MM-dd HH:mm:ss fff
        /// </summary>
        public const string yyyyMMddHHmmssfff = "yyyy-MM-dd HH:mm:ss fff";

        /// <summary>
        /// HH:mm
        /// </summary>

        public const string HHmm = "HH:mm";

        /// <summary>
        /// HH:mm:ss
        /// </summary>

        public const string HHmmss = "HH:mm:ss";

    }
}
